using System;
using Microsoft.EntityFrameworkCore;
using Sistema.Universitario.Infrastructure.Data;
using System.Collections.Generic;
using System.Threading.Tasks;

namespace Sistema.Universitario.Infrastructure.Repositories;

public class BaseRepository<T> where T : class
{
     protected readonly SistemaUniversitarioDbContext _context;
        protected readonly DbSet<T> _dbSet;

        public BaseRepository(SistemaUniversitarioDbContext context)
        {
            _context = context;
            _dbSet = _context.Set<T>();
        }

        public async Task<IEnumerable<T>> GetAllAsync()
        {
            return await _dbSet.ToListAsync();
        }

        public async Task<T> GetByIdAsync(Guid id)
        {
            return await _dbSet.FindAsync(id);
        }

        public async Task<T> AddAsync(T entity)
        {
            await _dbSet.AddAsync(entity);
            await _context.SaveChangesAsync();
            return entity;
        }

        public async Task<T> UpdateAsync(T entity)
        {
            _dbSet.Update(entity);
            await _context.SaveChangesAsync();
            return entity;
        }

        public async Task<bool> DeleteAsync(Guid id)
        {
            var entity = await _dbSet.FindAsync(id);
            if (entity == null) return false;

            _dbSet.Remove(entity);
            await _context.SaveChangesAsync();
            return true;
        }
}
